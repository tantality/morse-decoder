const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let str=[];
    str=expr.match(/.{1,10}/g).map(el=>el.replace(/10/g,'.')).map(el=>el.replace(/11/g,'-')).map(el=>el.replace(/0/g,'')).map(el=>el.replace(/[*]{10}/g,' '));
    str=str.map(item=>{ 
        if(item!=' ') return item.replace(item,MORSE_TABLE[item]);
        return ' ';
    }).join('');
    return str;
}

module.exports = {
    decode
}